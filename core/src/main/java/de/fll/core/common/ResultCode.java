package de.fll.core.common;

public enum ResultCode {
    SUCCESS(200, "success"),
    ERROR(500, "error");

    private final int code;
    private final String message;

    ResultCode(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }
}
